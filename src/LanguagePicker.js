/* ---------------- TRANSLATION FACTORY ---------------- */
export function tFactory(language) {
  return function (key, vars = {}) {
    let text =
      translations[language]?.[key] ||
      translations["English"][key] ||
      key;

    // Replace {x} with dynamic values
    Object.keys(vars).forEach((k) => {
      text = text.replace(`{${k}}`, vars[k]);
    });

    return text;
  };
}

